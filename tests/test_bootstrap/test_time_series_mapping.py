def test_transformations_to_string():
    config_row = TimeSeriesMappingEntry(
        object_type="reservoir",
        object_name="1",
        attribute_name="inflow",
        time_series_external_id="random_ex_id",
        transformations=[
            Transformation(
                transformation=TransformationType.ADD_WATER_IN_TRANSIT,
                kwargs={
                    "gate_name": "b_Beito-Oyangen_Volbufjord(97)",
                    "external_id": "/Begna/Magasin/Beitoøyangen-lukesum-MIXmålt-m3s-1h-v",
                },
            ),
        ],
        retrieve=RetrievalType.RANGE,
    )

    transformation_string = config_row._transformations_to_strings(max_cols=4)

    expected_result = [
        '[{"transformation": "ADD_WATER_IN_TRANSIT", "kwargs": {"gate_name": "b_Beito-Oyangen_Volbufjord(97)", "external_id": "/Begna/Magasin/Beitoøyangen-lukesum-MIXmålt-m3s-1h-v"}}]',
        "",
        "",
        "",
    ]

    assert transformation_string == expected_result


@pytest.mark.parametrize("watercourse_name", ["OE", "Lågen", "Otta", "Glomma"])
def test_base_mapping_heco(watercourse_name):
    """
    Testing that the new (generated) mapping matches what we have approved earlier
    """
    case = "heco"
    config = BootstrapConfig.from_yamls(DATA / case)
    _ = [w.set_shop_yaml_paths(case) for w in config.watercourses]

    for watercourse_config in config.watercourses:
        if watercourse_config.name == watercourse_name:
            break
        watercourse_config = None

    with monkeypatch_cognite_client():
        client = CogniteClient()
    # Mocking client.time_series.list - used for mapping based on RRS time series found in CDF
    client.time_series.list.return_value = dummy_rrs_time_series(watercourse_config.rrs_ids)
    # Mocking client.time_series.retrieve_multiple - to avoid that we skip mappings because time series are not found
    client.time_series.retrieve_multiple.return_value = relevant_dummy_time_series()

    mapping = create_heco_mapping(
        client=client,
        rrs_ids=watercourse_config.rrs_ids,
        tco_paths=watercourse_config.tco_paths,
        raw_shop_case_path=watercourse_config.yaml_raw_path,
        hardcoded_mapping=watercourse_config.hardcoded_mapping,
        hist_flow_time_series=watercourse_config.hist_flow_timeseries,
    )

    print(mapping)

    # Convert to dataframe
    mapping_df = mapping.to_dataframe()
    mapping_df.sort_values(by="shop_model_path", inplace=True)
    mapping_df.reset_index(drop=True, inplace=True)

    if (
        not pd.read_csv(f"tests/test_bootstrap/{watercourse_config.name}_base_mapping.csv")
        .fillna("")  # Filling na/NaN/None with empty string to not .equals treat these as equal
        .equals(
            mapping_df.fillna(""),
        )
    ):
        # Writing new mapping to file, to enable manual comparison with "approved" version
        mapping_df.to_csv(f"tests/test_bootstrap/{watercourse_config.name}_base_mapping_new.csv", index=False)
        raise RuntimeError(
            f"New time series mapping differs from approved version. Compare tests/test_bootstrap/{watercourse_config.name}_base_mapping.csv and tests/test_bootstrap/{watercourse_config.name}_base_mapping_new.csv, and replace the former with the latter if the new mapping looks more correct."
        )


